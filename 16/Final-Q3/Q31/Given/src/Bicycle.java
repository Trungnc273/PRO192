public class Bicycle 
{
    private float price;
    private String name;

    public Bicycle() 
    {
    }

    public Bicycle(String name, float price) 
    {
        this.price = price;
        this.name = name;
    }

    public float getPrice() 
    {
        return price;
    }

    public String getName() 
    {
        return name;
    }
    
}
